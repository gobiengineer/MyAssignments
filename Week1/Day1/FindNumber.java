public class FindNumber {
	public static void main(String[] args) {
		int num =45;
		if (num>0) {
			System.out.println("Number is Positive:" +num);
		}
			else if (num==0) {
				System.out.println("Number is Neutral:" +num);
				
			}
			else {
				System.out.println("Number is Negative:" +num);
			}
			
		}

}
